/// Attempts to translate a tag and type pairs into a property name
pub fn get_tag_name(tag: u16, ptype: u16) -> Option<&'static str> {
    TAG_MAP
        .binary_search_by(|m| (m.0).cmp(&(tag, ptype)))
        .ok()
        .map(|idx| TAG_MAP[idx].1)
}

